<h2>Sending Fanmail</h2>

<p>You loves to hear from his fans, so don't be shy about contacting him! You can either contact him through the post or through his website.</p>

<h3>Post</h3>
<p>This address is simply Gackt's fanmail address, however you can direct your mail to You as well.</p>

<p><strong>Gackt Official FANCLUB Dears<br>
Shibuya-yuubinkyoku DOME, Shibuya-ku<br>
Tokyo 150-8681<br>
Japan</strong></p>

<ul>
	<li>Make sure you address it to You (or "YOU") on the <em>outside</em> of the envelope. For example, add "For YOU" as a second line to the address or write the same in the corner and back of the envelope.</li>
	<li>Put your name and address on the back of the envelope.</li>
	<li>If you can, get your letter translated into Japanese. If you can't translate your letter, see if a friend will help you. Do not use an automatic translation service (e.g. Google Translate), as there'll likely be many errors.</li>
	<li>If you can't get your letter translated, don't worry! Just keep your sentences short and easy to understand. I suggest writing in English, as he's probably most familiar with this than any other foreign language.</li>
	<li>Include a self-addressed envelope inside your letter (or provide your name and address). You never know, you may get a reply! You could also include other contact details such as your email address.</li>
	<li>Hand-write your letter if you can.</li>
	<li>Do not simply say "I love you!" or "I'm your biggest fan". Tell him why you're his fan and what you admire about him.</li>
	<li>If you want, send him some fanart, or art in general. As he has a love of photography, you could send him a photo of the city where you live, or your local area.</li>
</ul>

