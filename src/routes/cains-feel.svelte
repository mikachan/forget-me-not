<h3>First performance with Gackt</h3>

<figure>
	<img alt="Gackt and You performance at Christmas" class="p-4 pt-0" width="200" align="right" src="{hana}">
</figure>

<p>Gackt and You performed <em>hanashitaku wa nai</em>, originally by T-Bolan, on the Happy Xmas show 2003, with You on the guitar and Gackt singing. Ten years before then, around Christmas time, Gackt and You performed <em>hanashitaku wa nai</em> in Kamogawa, Kyoto. It was their first performance together. Gackt's girlfriend at the time had left him and so he was alone for Christmas Eve and Day. You asked Gackt if he'd sing along to him playing guitar, so he was not alone. Gackt refused, however You insisted on playing in the street to keep him company.</p>

<p>If you've never seen this clip, you can watch it on <a href="http://youtube.com/watch?v=Mufmw-UXY2Y" target="new">YouTube</a>.</p>