<h3>Meeting Gackt</h3>

<p>Gackt wrote about how he met You in his autobiography. He's mentioned in Section 2: Kakusei, Chapter 4: "Hatsu Bando CAIN'S FEEL".</p>

<p>Bascially, while Gackt was in Kyoto he visited a livehouse where a live performance was being held.  He was quite taken with one of the guitarists, so he asked around about the live event. In Gackt's autobiography, he says that at first You and him didn't have a very pleasant relationship. Gackt thought You wanted to fight, because You was glaring at him. You walked up to Gackt saying "Gla.. glasses! Glasses!" So, it turns out that he was glaring at Gackt because he couldn't see him clearly. They ended up fighting anyway after Gackt asked You about the live event. At first, Gackt didn't believe that he was the guitarist, so You took him to his house to show him pictures proving that it was him. They became friends and decided to form a band.  They needed a vocalist, and since there was no one else, You asked Gackt to do it. After more fighting, You was the first person to tell Gackt he had a good voice.</p>

