</ul><br>

<u>DVDs</u><br><br>

Of course, You appears in all of the concert DVDs so I can't really single one out. Although, there's a special place in my heart for <a href="http://www.cdjapan.co.jp/detailview.html?KEY=CRBP-10011" target="new">Kagen no Tsuki</a> and <a href="http://www.cdjapan.co.jp/detailview.html?KEY=CRBP-10020" target="new">The Sixth Day and Seventh Night</a>. I believe these are the best concerts so far, so if you can't decide which concert to buy, I recommend these two! Of course, if you're keeping up-to-date with Gackt's new releases, you might enjoy his latest concerts more than these.<br><br>

You also appears in a video on <a href="http://www.cdjapan.co.jp/detailview.html?KEY=CRVP-10006" target="new">Soyokaze</a>, however this is a VHS (NTSC).<br><br>

Don't forget <a href="http://global.yesasia.com/en/PrdDept.aspx/aid-10221/section-videos/code-j/version-all/pid-1003610281/" target="new">Moon Child</a>!<br><br>

<u>Photobooks</u><br><br>

I'm not a big fan of photobooks myself, however I couldn't resist buying a Gackt photobook. I now own four. =_= I always think they're too expensive just for a load of pictures, but when I look through them I do enjoy it and I certainly don't regret buying them. If there's a certain Gackt era you enjoy, do some research on any books he released around that time and get a photobook. ^^<br><br>

