<strong>Sport</strong>: I gather from everything that Gackt says, You looooves to snowboard. He can do a 360 in the air and probably a lot of other nifty tricks. He also taught Gackt to snowboard. I've also learnt that he likes to play tennis, bowling, baseball and a bunch of other sports as well. No wonder he's so skinny.

