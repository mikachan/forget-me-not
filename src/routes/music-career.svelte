<h2>Music Career</h2>

<p>You's best known for playing the guitar and violin, but can also play drums. At live shows he usually plays rhythm guitar and sometimes violin.</p>

<p>You also wrote a song for Chachamaru's album <em>Air</em>, called <em>Metamorphose</em>.</p>

<h3>CAINS:FEEL (1989-1995)</h3>

<p>You was a guitarist for a band in Kyoto when he first met Gackt. Shortly after meeting him, they became good friends and decided to start a band together.</p>

<p>You can read more about <a href="/cains-feel">CAINS:FEEL here</a>.</p>

<h3>GacktJOB (2000-2018)</h3>
<p>Five years after CAINS:FEEL, Gackt, You and Ren began GacktJOB. You was in JOB from the beginning of Gackt's solo career, first recording in LA in January 1999. He played rhythm guitar and violin.</p>

<p>It was around this time that You began to play violin and piano.</p>

<p>He raps with Yosh in <em>Dybbuk</em>, on Gackt's <em>Crescent</em> album. Him and Yosh take turns, and You starts. He also raps in <em>Redemption</em>, as well as appearing in the promotional video.</p>

<p>The other members of GacktJOB have said that You is like a robot, with the exception of Gackt, who says he's half donkey, half horse.</p>

<p>The members of GacktJOB celebrated You's 30th birthday on Gackt's radio show; I've dedicated a page to it <a href="/birthday-2003">here</a> since I had so much info about it.</p>

<p>You can read more about <a href="/gacktjob">GacktJOB here</a>.</p>

<h3>YELLOW FRIED CHICKENz (2010-2012)</h3>

<p>YELLOW FRIED CHICKENz (also known as "YFC" or "YFCz") began as a project by Gackt to express "the cowardice in men's souls". They released one studio album, one 'best of' album, a couple of singles and a live album from their European tour.</p>

<h3>S.Q.F (2017-present)</h3>

<p>S.Q.F is a Japanese band formed in 2000 by ex-MASCHERA vocalist michi. and was originally michi.'s solo project, with ex-iLLUMINA guitarist, Nao. The name S.Q.F stands for "Spinning Q Factor."</p>

<p>You has been a support band member since 2017, playing guitar at their live shows and on studio recordings.</p>