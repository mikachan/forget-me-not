<h3>First performance with Gackt</h3>

<figure>
	<img alt="Gackt and You performance at Christmas" class="p-4 pt-0" width="200" align="right" src="{hana}">
</figure>

<p>CAINS:FEEL was You's first band with Gackt, and their first public performance together was in 1993 when they performed <em>hanashitaku wa nai</em> (T-Bolan) in Kamogawa, Kyoto. Gackt's girlfriend at the time had left him and so he was alone on Christmas Day. You asked Gackt if he'd sing along to him playing guitar, so he was not alone. Gackt refused, however You insisted on playing in the street to keep him company. They recreated the performance on the Happy Xmas Show in 2003, with You on the guitar and Gackt singing.</p>

