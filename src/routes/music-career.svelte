<h2>Music Career</h2>

<p>When Gackt met You he was a guitarist for a band in Kyoto. The band members were known for starting fights at livehouses. I think they were also a visual kei band. I'm pretty sure he would've been able to play the violin at this time too.</p>

<p>You's famous for playing the guitar and violin, of course. However I've heard he can also play the drums.</p>

<p>I'm not putting a discography up here since there's one on nearly every Gackt fansite. You has worked on all of Gackt's solo work, playing the guitar. I don't think he plays all the violin pieces, but he has been credited for some of them.</p>

<p>You has been in all of Gackt's concerts and live house tours, playing rhythm guitar. In certain songs, such as <em>Miz√©rable</em> (The Sixth Day and Seventh Night, 2004) and <em>Juunigatsu no Love Song</em> (Kagen no Tsuki, 2002), he plays the violin instead (since it's impossible to play both at once, ne). Although, since JOB now only has You as the rhythm guitarist, he's nearly always on guitar.</p>

<p>You is also a lyricist. He wrote a song for Chachamaru's album <em>Air</em>, called <em>Metamorphose</em>. The lyrics can be seen <a href="http://www.mognet.net/artisttranslation.php?id=124&amp;translation=english" target="new">here</a>. You also helps with the violin scores for Gackt's compositions. Actually, I'm not sure if he just helps Gackt, or writes all of the violin pieces.</p>

<h3>GacktJOB</h3>
<p>Five years after <a href="/cainsfeel/">CAINS:FEEL</a>, Gackt got in touch with You and Ren again to form GacktJOB.</p>

<p>You has been in JOB since the start of Gackt's solo career, first recording in LA in January 1999.  He plays the beat guitar and violin. I love the way he plays the guitar live. He really is a robot!</p>

<p>He raps with Yosh in <em>Dybbuk</em>, on Gackt's <em>Crescent</em> album. I think him and Yosh take turns, and You starts. He also raps in <em>Redemption</em>, as well as appearing in the promotional video.</p>

<p>The other members of GacktJOB say that You is a robot, with the exception of Gackt, who says he's half donkey, half horse.  I'd probably say he was a robot because of the way he acts in some lives.</p>

<p>This is what You says about the other members of JOB:</p>

<ul>
	<li>Gackt: as wide and as deep as the ocean.</li>
	<li>Chacha: a very elegant and talented guitarist.</li>
	<li>Masa: pretty and sexy, but also like a boy.</li>
	<li>Ren: adorable and optimistic, but "yellow"</li>
	<li>Toshi: like a dependable big bro.</li>
	<li>Igao: optimistic like the sun.</li>
</ul>

<p>You is also the only member of JOB who hasn't pursued a solo career. His loyalty to Gackt and JOB has earnt him a lot respect, and quite a lot of fans. As he's been with Gackt since the beginning of his solo career, fans have grown to love him as both a musican and a solid part of JOB. It is difficult to imagine the band without him; it just wouldn't be the same. I'm sure we've got nothing to worry about!</p>

<p>I'd be interested to know about You's relationships with the other JOB members besides Gackt. I think him and Chacha are very good friends. I know that Gackt once said Chacha is always trying to get You's attention. Didn't Chacha try to seduce You one time, too? Chacha also taught You a lot of his guitar skills. They've known each other for a long time, so no doubt they are close.</p>

<p>I believe he's still good friends with Masa, as he was often seen at Spiky lives. One time, when Masa was still in JOB, the band were sleeping in the same room. Masa decided to poke You's foot with a pencil to see if he woke up. The next day, You woke up with a pencil mark on his foot.</p>

<p>I'm guessing he's good friends with Ren, since they've know each other since they were teenagers. You also see him hanging around with Tetsuro, too. Tetsuro acts in the <em>ANOTHER WORLD</em> PV and Moon Child (the little, goofy guy). I don't have a clue about Ryu, Toshi or Ju-ken...</p>

