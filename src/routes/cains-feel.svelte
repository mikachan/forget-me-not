<h3>First performance with Gackt</h3>

<figure>
	<img alt="Gackt and You performance at Christmas" class="p-4 pt-0" width="200" align="right" src="{hana}">
</figure>

<p>As I'm sure you all know and have downloaded and watched this clip a million times (or was that just me?), Gackt and You performed <em>hanashitaku wa nai</em>, originally by T-Bolan, on the Happy Xmas show 2003, with You on the guitar and Gackt singing. Ten years before then, around Christmas time, Gackt and You performed <em>hanashitaku wa nai</em> in Kamogawa, Kyoto. It was their first performance together.  Gackt's girlfriend at the time had dumped him and therefore he was alone for Christmas Eve and Day. You asked Gackt if he'd sing along to him playing guitar, so he was not alone. Gackt refused as You had a girlfriend at the time. However You insisted on playing in the street. Gackt still has a vivid memory of their first performance together, and often mentions it in interviews around Christmas time. He always says how nice it was of You to give up his Christmas for him.</p>

<p>If you've never seen this clip, you can watch it on <a href="http://youtube.com/watch?v=Mufmw-UXY2Y" target="new">YouTube</a>.</p>