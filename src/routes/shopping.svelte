<h1>Shopping</h1>

There are several places you can buy Gackt releases online. <br>

<ul>
	<li><a href="http://www.yesasia.com/" target="new">YesAsia</a></li>
	<li><a href="http://www.cdjapan.co.jp/" target="new">CD Japan</a></li>
	<li><a href="http://www.amazon.co.jp/" target="new">Amazon Japan</a></li>
	<li><a href="http://www.hmv.co.jp/" target="new">HMV Japan</a></li>
	<li><a href="http://www.jlist.com/" target="new">JList</a></li>
</ul>

My personal favourite is YesAsia and I would recommend them to anyone, anywhere in the world. I have used CD Japan before, however the package took nearly three months to arrive (they had it in stock) and I had to pay extra shipping costs when I received it from the post office. Although, this may have been a one-off. I would use them again with caution. The online experience is fine! They also stock items that you may not find elsewhere.<br><br>

I do not recommend eBay for Gackt purchases, as there are many bootlegs around. This is certainly the place to find a bargain, though. Just be careful what you're spending your money on. The general rule for spotting unlicensed Gackt products is anything produced by <a href="http://miyacd.com/" target="new">Miya Records</a>. They often seem (and sometimes are) high-quality, however if the <i>Dears</i> or <i>Nippon Crown</i> logos are not present then it's likely they're a bootleg.<br><br>

Late 2007, some Gackt releases became available in America and Europe. If you can, try to buy products from a more local source rather than importing. This encourages the record companies to sell stuff overseas! If you prefer to download, Gackt's music is also available through iTunes.<br><br>

<h3>Recommendations</h3><br>

I strongly encourage you to buy any and all of Gackt's work that you enjoy. Although, not everyone can afford to buy all of his work and therefore you may be a bit choosey. I cannot recommend any of Gackt's albums, photobooks or DVDs enough. <i>However</i>, there are several products that may satisfy the inner You fan more than others. ~_^<br><br>

<u>Albums</u><br><br>

<ul>
	<li><a href="http://global.yesasia.com/en/PrdDept.aspx/aid-10221/section-music/code-j/version-all/did-20/pid-1001834147/" target="new">MOON</a> - Gackt's third solo album. There are images of You in the CD booklet.</li>
