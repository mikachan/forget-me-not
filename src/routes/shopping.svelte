<u>Platinum Box Sets</u><br><br>

These are very expensive pieces of merchandise! Make sure you definately <i>want</i> it before buying it. They are certainly very nice box sets and are beautifully packaged, but they are just a DVD and a special gift (e.g. a clock). I love them and have every one of them, but I don't think they're worth the cost. However, that's the price you pay for stuff like this, eh? ^^; There are loads of bootleg versions floating around, so if you find one for below ï¿½20/$30 ish on eBay, then it may not be the real thing. However, most are still available on YesAsia and CD Japan.<br><br>

